
public class OutlineTringle {
	public static void main(String[] args) {
			for(int i=1;i<=10;i++) {
				for(int j=1;j<=19;j++) {
					if((i==10) || (i+j==11) || (i-j==9))
					System.out.print("*");
				
				else {
					System.out.print(" ");
				}
			}System.out.println();
			}

		}

	}
	
	
	
	

